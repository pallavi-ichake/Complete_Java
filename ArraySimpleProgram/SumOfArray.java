package ArraySimpleProgram;

import java.util.Scanner;

public class SumOfArray 
{
		  public static void main(String args[]) 
		  {
		    int n = 0, sum = 0;
		    int a[] = {1,5,9,4,8,3,6};
		    for (int i = 0; i < n; i++)
		    {
		      a[i] = n;
		    }
		    for (int num: a)
		    {
		      sum = sum + num;
		    }
		    System.out.println("Sum of array elements is :" + sum);
		  }
}


